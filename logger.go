package main

import (
	"errors"
	"fmt"
	"log"
)

// Log the given formatted string with its parameters, and return it
// as a new error.
func loggedError(format string, params ...interface{}) error {
	str := fmt.Sprintf(format, params...)
	log.Print(str)
	return errors.New(str)
}
